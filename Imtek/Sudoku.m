sudokuDigitQ[d_Integer]:=MemberQ[Range[9],d];

rcToBlock[r_?sudokuDigitQ,
      c_?sudokuDigitQ]:=(Quotient[r-1,3]3)+(Quotient[c-1,3]+1);

rcToEntry[r_?sudokuDigitQ,
      c_?sudokuDigitQ]:=(Quotient[r-1,1]9)+(Quotient[c-1,1]+1);

makeIntegerSudoku[
      p:{{_String,_String,_String,_String,_String,_String,_String,_String,_\
String}..}]:=Map[If[#=="."||#==" "||#=="0",0,ToExpression[#]]&,p,{2}];

generalConstraint[p_]:=Normal[SparseArray[{p\[Rule]1},81]];
rowConstraint[r_,d_]:=generalConstraint[((d-1)9+r)];
columnConstraint[c_,d_]:=generalConstraint[((d-1)9+c)];
blockConstraint[{r_,c_},d_]:=generalConstraint[((d-1)9+rcToBlock[r,c])];
cellConstraint[{r_,c_},d_]:=generalConstraint[rcToEntry[r,c]];

makeGivenLine[{r_?sudokuDigitQ,c_Integer?sudokuDigitQ},d_Integer]:=
    Join[rowConstraint[r,d],columnConstraint[c,d],blockConstraint[{r,c},d],
      cellConstraint[{r,c},d]];

freeMovesSudoku[
      p:{{_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_\
Integer,_Integer}..}]:=
    Module[{rowSets,colSets,blockSets,cellSets,makeCellSet},
      
      makeCellSet[d_,{r_?sudokuDigitQ,c_?sudokuDigitQ}]:=If[d\[Equal]0,
          
          Intersection[rowSets[[r]],colSets[[c]],
            blockSets[[rcToBlock[r,c]]]],{d}
          ];
      
      rowSets=
        Complement[Range[9],#]&/@(Select[Union[#],(#\[NotEqual]0)&]&/@p);
      colSets=
        Complement[Range[9],#]&/@(Select[Union[#],(#\[NotEqual]0)&]&/@
              Transpose[p]);
      blockSets=
        Flatten[Map[
            Complement[Range[9],
                Select[Flatten[Transpose[#]],(#\[NotEqual]0)&]]&,
            Partition[#,3]&/@(Transpose[#]&/@Partition[p,3]),{2}],1];
      
      Return[MapIndexed[makeCellSet,p,{2}]]
      ];

extractSolution[
      sol:{{_Integer,_Integer,_Integer,_Integer}..}]:=(Quotient[#-1,9]+1)&/@
      Flatten[#[[1]]&/@Sort[sol,(#1[[-1]]<#2[[-1]])&]];

makeTableLine[ds:{(_Integer)..},{r_,c_}]:=makeGivenLine[{r,c},#]&/@ds;



