imsReadSudoku[f_String]:=Module[{s,res},
      s=OpenRead[f];
      res=
        Partition[
          Flatten[ReadList[s,Character,RecordSeparators\[Rule]{"\n"},
              RecordLists\[Rule]True]],9];
      Close[s];
      Return[makeIntegerSudoku[res]]
      ];

imsDrawSudoku[
      p:{{_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_\
Integer,_Integer}..}]:=Module[{drawLabel,grid},
      drawLabel[l_Integer,{i_Integer,j_Integer}]:=
        Text[If[l\[Equal]0," ",ToString[l]],{j,10-i},{0,0}];
      grid={Thickness[.004],Line[{{.5,#-.5},{9.5,#-.5}}]&/@Range[10],
          Line[{{#-.5,.5},{#-.5,9.5}}]&/@Range[10],Thickness[.012],
          Line[{{.5,3#-2.5},{9.5,3#-2.5}}]&/@Range[4],
          Line[{{3#-2.5,.5},{3#-2.5,9.5}}]&/@Range[4]};
      Graphics[{grid,MapIndexed[drawLabel,p,{2}]},AspectRatio\[Rule]Automatic,
        PlotRange\[Rule]All]
      ];

imsSolveSudoku[
      p:{{_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_Integer,_\
Integer,_Integer}..}]:=
    Module[{exactCoverTable,theSolutions,myRecursionLimit},
      exactCoverTable=
        Flatten[MapIndexed[makeTableLine,freeMovesSudoku[p],{2}],2];
      myRecursionLimit=$RecursionLimit;
      $RecursionLimit=10000;
      theSolutions=imsExactCover[exactCoverTable];
      $RecursionLimit=myRecursionLimit;
      Partition[extractSolution[#],9]&/@theSolutions
      ];



